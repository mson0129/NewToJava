public class ENotation {
    public static void main(String[] args)
    {
        double e1 = 1.2e-3;
        double e2 = 1.2e+3;

        int num1 = 0xA0E;
        int num2 = 0752;

        System.out.println(e1); // 0.0012
        System.out.println(e2); // 1200.0
        System.out.println(num1); // 2574
        System.out.println(num2); // 490
    }
}
